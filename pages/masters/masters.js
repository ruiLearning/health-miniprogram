function inferCategory(article) {
  const text = `${article.title} ${article.tags.join(' ')} ${article.intro}`
  if (/断食|减脂|生酮|低碳|餐盘|热量|地中海|高蛋白|容量饮食|原型食物|步行 \+ 力量/.test(text)) return 'fatloss'
  if (/饮水|肠道|控糖|早餐|营养|饮食/.test(text)) return 'nutrition'
  if (/睡眠|晨光|晒太阳|作息|恢复/.test(text)) return 'routine'
  if (/久坐|习惯|活动量|办公室/.test(text)) return 'habit'
  if (/压力|情绪|体重不是全部|认知|健康观/.test(text)) return 'mindset'
  return 'nutrition'
}

