const ARTICLES = module.exports.ARTICLES = [...FEATURED_ARTICLES, ...METHOD_ARTICLES].map(article => ({
  ...article,
  category: inferCategory(article)
}))

Page({
  data: {
    articles: [],
    filteredArticles: [],
    categories: CATEGORY_META,
    activeCategory: 'all'
  },

  onLoad() {
    this.setData({ articles: ARTICLES })
    this.applyCategory('all')
  },

  applyCategory(category) {
    const filteredArticles = category === 'all'
      ? ARTICLES
      : ARTICLES.filter(item => item.category === category)
    this.setData({
      activeCategory: category,
      filteredArticles
    })
  },

  selectCategory(e) {
    const category = e.currentTarget.dataset.category
    this.applyCategory(category)
  },

  goDetail(e) {
    const id = e.currentTarget.dataset.id
    const article = ARTICLES.find(item => item.id === id)
    if (article) {
      wx.setStorageSync('hc_master_article', article)
    }
    wx.navigateTo({
      url: `/pages/masters/detail/detail?id=${id}`
    })
  }
})
